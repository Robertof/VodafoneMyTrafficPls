    private void formWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosing
        if (t1.isAlive())
        {
            threadHasToStop = true;
            t1.interrupt();
        }
        if (autoUpdButton.isSelected())
        {
            // check again if data is valid
            try {
                int value = Integer.parseInt(jSpinner1.getValue().toString());
                if (value <= 0)
                    throw new NumberFormatException();
                // register the timer
                // get the real value in seconds
                int multiplier = 1;
                if (jComboBox1.getSelectedIndex() == 1) // minutes
                    multiplier = 60;
                else if (jComboBox1.getSelectedIndex() == 2) // hours
                    multiplier = 3600;
                else if (jComboBox1.getSelectedIndex() == 3) // days
                    multiplier = 86400;
                mainClass.registerTimer(value * multiplier);
                System.out.println ("Timer d" + (value * multiplier) + " registered");
            } catch (NumberFormatException e)
            {
                Main.throwError("Impossibile impostare un valore per il timer.");
            }
        }
        else
        {
            // try to unregister timer
            mainClass.unregisterTimer();
        }
        mainClass.setEnabled(true);
        this.setVisible(false);
        this.dispose();
    }//GEN-LAST:event_formWindowClosing

