    public AutoUpdateGUI(GUI mC) {
        initComponents();
        mainClass = mC;
        mainClass.setEnabled(false);
        t1 = new Thread (new Runnable(){
            @Override
            public void run() {
                // this thread checks the spinner value
                while (true)
                {
                    if (threadHasToStop)
                    {
                        threadHasToStop = false;
                        return;
                    }
                    // get spinner value
                    try {
                        int value = Integer.parseInt(jSpinner1.getValue().toString());
                        if (value < 0 || value == 0)
                            jSpinner1.setValue(1);
                        Thread.sleep(300);
                    }
                    catch (NumberFormatException e)
                    {
                        jSpinner1.setValue(1);
                    }
                    catch (InterruptedException e)
                    {}
                }
            } 
        });
        t1.start();
        if (mainClass.timerFreq > 0 && mainClass.existingTimer != null && mainClass.existingTimer.isAlive())
        {
            // update our fields with the values in the mainthread
            autoUpdButton.setSelected(true);
            if (mainClass.timerFreq < 60)
            {
                System.out.println("Setting to => " + mainClass.timerFreq);
                jSpinner1.setValue(mainClass.timerFreq);
            }
            else if (mainClass.timerFreq > 60 && mainClass.timerFreq < 3600)
            {
                jSpinner1.setValue(Math.round(mainClass.timerFreq / 60));
                jComboBox1.setSelectedIndex(1);
            }
            else if (mainClass.timerFreq > 3600 && mainClass.timerFreq < 86400)
            {
                jSpinner1.setValue(Math.round(mainClass.timerFreq / 3600));
                jComboBox1.setSelectedIndex(2);
            }
            else if (mainClass.timerFreq > 86400)
            {
                jSpinner1.setValue(Math.round(mainClass.timerFreq / 86400));
                jComboBox1.setSelectedIndex(3);
            }
        }
    }

